def main():
    fs = [sc["f"] for sc in SCENES]
    missing = [f for f in fs if f not in SFX]
    extra = [f for f in SFX if f not in fs]
    assert not missing, f"thiếu SFX cho entry: {missing}"
    assert not extra, f"SFX trỏ entry không phải đầu scene: {extra}"
    data = {"sfx": [{"stt": i + 1, "keywords": SFX[f]} for i, f in enumerate(fs)]}
    out = ROOT / "output" / "sfx.json"
    out.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    n_empty = sum(1 for e in data["sfx"] if not e["keywords"])
    print(f"WROTE {len(data['sfx'])} scene ({n_empty} scene keywords rỗng)")


if __name__ == "__main__":
    main()
